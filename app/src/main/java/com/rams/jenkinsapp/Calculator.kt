object Calculator {

    fun addition(first: Int, second: Int) : Int {
        return first + second
    }
    fun multiplication(first: Int, second: Int) : Int {
        return first * second
    }
    fun subtraction(first: Int, second: Int) : Int {
        return first - second
    }
}